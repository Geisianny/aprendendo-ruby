#Iteração: for, while,Times, do/while

#for
fruits = ["Maça","Uva", "morango", "tomate"]

for fruit in fruits
    puts(fruit)
end

#while
x = 1
while x < 10
    puts(x)
    x +=1
end
puts()

#loop do
count = 1
loop do
    puts (count)
    if(count == 10)
        break
    end
    count += 1
end

#Times
